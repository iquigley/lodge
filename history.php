                <div class="history-section">
                    <span class="era-badge">1717 &mdash; The Grand Lodge</span>
                    <h2>The Enlightenment Lodge: Reason, Liberty, Brotherhood</h2>
                    <p>
                        On the feast of St. John the Baptist, June 24, 1717, four London lodges
                        gathered at the Goose and Gridiron alehouse in St. Paul's Churchyard and
                        formed the Premier Grand Lodge of England — the first formal governing
                        body of Freemasonry as we know it today. It was no accident that this
                        moment arrived at the height of the Enlightenment, that great intellectual
                        revolution sweeping Europe with its torch of reason, empirical inquiry,
                        and natural rights.
                    </p>
                    <p>
                        The Masonic lodge became a perfect vessel for Enlightenment ideals.
                        At the lodge table, a lord and a tradesman could sit as equals — a
                        radical proposition in the rigid class hierarchies of 18th-century
                        Europe. The lodge demanded of its members a belief in God but imposed
                        no particular creed, welcoming men of every religious tradition at a
                        time when sectarian strife tore nations apart. It cultivated rational
                        discourse, banned discussion of politics and religion within its walls
                        to prevent division, and organized itself through democratic procedure.
                    </p>
                    <p>
                        James Anderson's <em>Constitutions of 1723</em> codified these principles,
                        articulating Masonry's commitment to moral virtue, fraternal duty, and
                        toleration — a manifesto as much as a rulebook, and one whose echoes
                        would be heard across the Atlantic within a generation.
                    </p>
                    <p>
                        Among the most illustrious Masons of the Enlightenment era was Voltaire
                        — the great French philosopher, satirist, and champion of civil liberties.
                        He was initiated into the Lodge des Neuf Soeurs in Paris in 1778, just
                        weeks before his death, in a ceremony attended by Benjamin Franklin himself.
                        The two titans of Enlightenment thought, meeting in a Masonic lodge,
                        embodied the transatlantic flow of ideas that would forge a new republic.
                    </p>
                </div>

