                <div class="history-section">
                    <span class="era-badge">17th Century</span>
                    <h2>The Quiet Transformation: From Stone to Symbol</h2>
                    <p>
                        As the great cathedral-building projects wound down and the guild
                        system began to weaken across Europe, something remarkable occurred
                        within the lodges of England and Scotland. Men who were not stonemasons
                        at all — scholars, gentlemen, noblemen, and philosophers — began to
                        be admitted as "accepted" or "speculative" members, drawn to the
                        fraternity not for its craft knowledge but for its symbolic language
                        and its spirit of brotherhood.
                    </p>
                    <p>
                        By the early 17th century, a quiet transition was underway. The tools
                        and vocabulary of operative masonry were being transmuted into a rich
                        allegorical system for moral instruction and philosophical inquiry.
                        The Lodge, once a worksite structure, became a sanctuary for free
                        thought at a time when religious and political orthodoxy could
                        still exact a deadly price for dissent.
                    </p>
                    <p>
                        Elias Ashmole — antiquary, astrologer, and founder of Oxford's
                        Ashmolean Museum — recorded his initiation into a lodge in Warrington,
                        England in 1646, one of the earliest confirmed records of a
                        non-operative Mason. The age of Speculative Masonry had begun.
                    </p>
                </div>

                <hr class="gold-divider">

