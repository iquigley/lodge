                    <span class="era-badge">1730s – 1800s</span>
                    <h2>The Masonic Republic: Freemasonry and the American Founding</h2>
                    <p>
                        When the first Masonic lodges appeared in the American colonies in
                        the 1730s — Philadelphia in 1731, Boston in 1733 — they arrived into
                        a society already hungry for the Enlightenment ideals they embodied.
                        The lodges became gathering places for the colonial elite and the
                        emerging professional class alike: merchants, lawyers, physicians,
                        printers, and military officers.
                    </p>
                    <p>
                        No fewer than nine signers of the Declaration of Independence were
                        confirmed Masons. Thirteen of the thirty-nine signers of the Constitution
                        held Masonic membership. The roll call of Masonic founding fathers reads
                        like the guest list at the constitutional convention: Benjamin Franklin,
                        a Grand Master who used his lodge in Paris as a salon for revolutionary
                        diplomacy; John Hancock, who presided over Boston's St. Andrew's Lodge;
                        Paul Revere, its Grand Master; and John Paul Jones, the father of the
                        American Navy.
                    </p>
                    <p>
                        Most towering among them was <strong class="text-cream">George Washington</strong>.
                        Initiated at the age of 20 in Fredericksburg, Virginia, Washington wore
                        his Masonic apron at the laying of the cornerstone of the United States
                        Capitol on September 18, 1793 — a ceremony conducted with full Masonic
                        honors. He was a lifelong and devoted Mason who saw no contradiction
                        between the Brotherhood's principles and the ideals of the new nation
                        he commanded: both were built on reason, virtue, and the conviction
                        that free men, voluntarily bound by their word, could govern themselves.
                    </p>
                    <p>
                        The Masonic influence on the architecture of American governance is
                        more than symbolic. The concepts embedded in Masonic ritual —
                        equality under the level, governance by ballot, the protection
                        of conscience from coercion — flowed directly into the intellectual
                        current that produced the Declaration and the Constitution.
                        Thomas Jefferson, though not a Mason himself, moved in deeply Masonic
                        social circles and drew on the same Enlightenment wellspring.
                    </p>
                </div>

