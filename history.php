                    <span class="era-badge">12th – 16th Century</span>
                    <h2>The Operative Craft: Stonemasons of the Cathedral Age</h2>
                    <p>
                        The roots of Freemasonry wind back through centuries to the guilds of
                        operative stonemasons — the men who raised the cathedrals, castles, and
                        great public buildings of medieval Europe. These skilled craftsmen organized
                        themselves into lodges, temporary structures erected at the foot of
                        construction sites, where they could eat, rest, store their tools, and
                        receive their wages.
                    </p>
                    <p>
                        Within the lodge, the craft was governed by strict rules of admission,
                        conduct, and secrecy. Apprentices served years of training before
                        being advanced to the rank of Fellowcraft, and only the most accomplished
                        were recognized as Master Masons, capable of overseeing the geometry and
                        architecture of an entire edifice. The secrets of their craft — the
                        measurements, the proportions, the structural techniques — were among
                        the most valuable intellectual property of the age, and they guarded
                        them accordingly.
                    </p>
                    <p>
                        The mason's tools — the square, the compass, the plumb, the level —
                        were not merely instruments of stone and mortar. They were also
                        symbols of moral precision: the square to ensure right conduct, the
                        compass to circumscribe desire within the bounds of virtue, the level
                        to remind all men of their equality before God and death.
                    </p>
